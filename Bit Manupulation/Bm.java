

public class Bm {
    public static void main(String args[]) {

        /*******Bitwise Operators***********/

        // Binary AND '&'
        System.out.println(5&6);
        // Binary OR '|'
        System.out.println(5|6);
        // Binary XOR '^'
        System.out.println(5^6);
        // Binary One's Compliment '~'
        System.out.println(~7);
        // Binary Left Shift
        System.out.println(4<<2);
        // Binary Right Shift
        System.out.print(4>>2);
    }
    
}
